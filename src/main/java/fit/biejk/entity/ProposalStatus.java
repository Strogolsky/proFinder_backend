package fit.biejk.entity;

/**
 * Enumeration representing possible statuses of an order proposal.
 */
public enum ProposalStatus {

    /**
     * Initial status when a proposal is created.
     */
    CREATED,

    /**
     * Status indicating the proposal has been approved.
     */
    APPROVED,

    /**
     * Status indicating the proposal has been rejected.
     */
    REJECTED
}
